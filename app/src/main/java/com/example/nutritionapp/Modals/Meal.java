package com.example.nutritionapp.Modals;

public class Meal {
    String name;
    String description;
    int carbo;
    int prot;
    int vitamina;
    int vitaminb;
    int vitaminc;
    int vitammine;
    int sodium;
    int iron;
    int fibre;
    int calcium;
    int calorie;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getCarbo() {
        return carbo;
    }

    public void setCarbo(int carbo) {
        this.carbo = carbo;
    }

    public int getProt() {
        return prot;
    }

    public void setProt(int prot) {
        this.prot = prot;
    }

    public int getVitamina() {
        return vitamina;
    }

    public void setVitamina(int vitamina) {
        this.vitamina = vitamina;
    }

    public int getVitaminb() {
        return vitaminb;
    }

    public void setVitaminb(int vitaminb) {
        this.vitaminb = vitaminb;
    }

    public int getVitaminc() {
        return vitaminc;
    }

    public void setVitaminc(int vitaminc) {
        this.vitaminc = vitaminc;
    }

    public int getVitammine() {
        return vitammine;
    }

    public void setVitammine(int vitammine) {
        this.vitammine = vitammine;
    }

    public int getSodium() {
        return sodium;
    }

    public void setSodium(int sodium) {
        this.sodium = sodium;
    }

    public int getIron() {
        return iron;
    }

    public void setIron(int iron) {
        this.iron = iron;
    }

    public int getFibre() {
        return fibre;
    }

    public void setFibre(int fibre) {
        this.fibre = fibre;
    }

    public int getCalcium() {
        return calcium;
    }

    public void setCalcium(int calcium) {
        this.calcium = calcium;
    }

    public int getCalorie() {
        return calorie;
    }

    public void setCalorie(int calorie) {
        this.calorie = calorie;
    }

}
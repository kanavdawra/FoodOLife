package com.example.nutritionapp.Modals;

public class piechartgraph {
    int fat;
    int carbohydates;
    int protein;
    int amount;
    int totalcalorie;


    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getTotalcalorie() {
        return totalcalorie;
    }

    public void setTotalcalorie(int totalcalorie) {
        this.totalcalorie = totalcalorie;
    }


    public int getfat() {
        return fat;
    }

    public void setfat(int fat) {
        this.fat = fat;
    }

    public int getCarbohydates() {
        return carbohydates;
    }

    public void setCarbohydates(int carbohydates) {
        this.carbohydates = carbohydates;
    }

    public int getProtein() {
        return protein;
    }

    public void setProtein(int protein) {
        this.protein = protein;
    }




}

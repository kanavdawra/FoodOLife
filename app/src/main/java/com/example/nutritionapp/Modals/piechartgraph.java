package com.example.nutritionapp.Modals;

public class piechartgraph {
    double fat;
    double carbohydates;
    double protein;
    double amount;
    double totalcalorie;

    public double getFat() {
        return fat;
    }

    public void setFat(double fat) {
        this.fat = fat;
    }

    public double getCarbohydates() {
        return carbohydates;
    }

    public void setCarbohydates(double carbohydates) {
        this.carbohydates = carbohydates;
    }

    public double getProtein() {
        return protein;
    }

    public void setProtein(double protein) {
        this.protein = protein;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public double getTotalcalorie() {
        return totalcalorie;
    }

    public void setTotalcalorie(double totalcalorie) {
        this.totalcalorie = totalcalorie;
    }





}
